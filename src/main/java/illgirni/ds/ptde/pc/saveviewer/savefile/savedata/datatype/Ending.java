/**
 * The game's endings.
 * 
 * @author illgirni
 *
 */
public enum Ending {
    /**
     * When game not finished once with the character.
     */
    NONE,
    
    /**
     * "Good" ending - linking the fire.
     */
    LORD_S_SUCCESSOR,
    
    /**
     * "Bad" ending - leaving the kiln.
     */
    DARK_LORD
}
